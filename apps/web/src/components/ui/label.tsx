import type * as React from "react";

import { cn } from "../../lib/utils.ts";

export type LabelProps = Omit<React.ComponentProps<"label">, "htmlFor"> & {
  htmlFor: string;
};

function Label({ className, children, htmlFor, ...props }: LabelProps) {
  return (
    <label
      htmlFor={htmlFor}
      className={cn(
        "text-sm font-medium leading-none text-zinc-900 peer-disabled:cursor-not-allowed peer-disabled:opacity-70",
        className,
      )}
      {...props}
    >
      {children}
    </label>
  );
}

export { Label };
